/**
 * 두 숫자의 합을 계산합니다.
 * @param {number} left 왼쪽 피연산자입니다.
 * @param {number} right 오른쪽 피연산자입니다.
 * @returns {number} 두 입력값의 합입니다.
 */
export function sum(left, right) {
  return left + right;
}

/**
 * 첫 번째 숫자에서 두 번째 숫자를 뺍니다.
 * @param {number} left 피감수입니다.
 * @param {number} right 감수입니다.
 * @returns {number} 두 입력값의 차입니다.
 */
export function sub(left, right) {
  return left - right;
}
