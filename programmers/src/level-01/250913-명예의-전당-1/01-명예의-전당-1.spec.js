import { solution } from './01-명예의-전당-1';

/**
k	  score                                            result
3	  [10, 100, 20, 150, 1, 100, 200],                 [10, 10, 10, 20, 20, 100, 100]
4	  [0, 300, 40, 300, 20, 70, 150, 50, 500, 1000],   [0, 0, 0, 0, 20, 40, 70, 70, 150, 300]
 */

describe('250913 / 01-명예의-전당-1', () => {
  it('solution() === [10, 10, 10, 20, 20, 100, 100]', () => {
    const result = solution(3, [10, 100, 20, 150, 1, 100, 200]);
    expect(result).toEqual([10, 10, 10, 20, 20, 100, 100]);
  });

  it('solution() === ', () => {
    const result = solution(4, [0, 300, 40, 300, 20, 70, 150, 50, 500, 1000]);
    expect(result).toEqual([0, 0, 0, 0, 20, 40, 70, 70, 150, 300]);
  });
});
